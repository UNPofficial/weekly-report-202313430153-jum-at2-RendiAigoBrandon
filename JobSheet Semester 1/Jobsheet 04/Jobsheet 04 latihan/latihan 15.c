void main(){
	
	int a = 7;
	
	// increment a
	a++;
	
	printf("a++ = %d\n", a);
	
	// increment a
	++a;
	
	printf("++a = %d\n", a);
	
	// decrement a
	a--;
	
	printf("a-- = %d\n", a);
	
	// decrement a
	--a;
	
	printf("--a = %d\n", a);

}